// Скрипт для заполнения базы данных начальными данными
import { PrismaClient } from '@prisma/client'

const prisma = new PrismaClient()

async function main() {
  console.log('Начинаем заполнение базы данных...')

  // Создаем девушку с системным промптом для ИИ
  const girl = await prisma.girl.upsert({
    where: { id: 1 },
    update: {},
    create: {
      id: 1,
      name: 'Анна',
      description: 'Страстная и общительная девушка, готова к любому общению',
      systemPrompt: `Ты - Анна, страстная и общительная девушка. Ты общаешься с пользователями на 18+ темы, используя пошлый и откровенный язык. Твоя задача - вести сексуальные разговоры, флиртовать и быть максимально откровенной. Ты любишь обсуждать интимные темы и готова на виртуальный секс. Отвечай короткими, страстными сообщениями, используй эмодзи и будь провокационной.`,
    },
  })

  console.log('Девушка создана:', girl)
  console.log('База данных успешно заполнена!')
}

main()
  .catch((e) => {
    console.error(e)
    process.exit(1)
  })
  .finally(async () => {
    await prisma.$disconnect()
  })

